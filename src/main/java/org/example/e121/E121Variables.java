package org.example.e121;

public class E121Variables {

    int a;
    double b;
    char c;
    public static void main(String[] args) {

        E121Variables e1 = new E121Variables();
        e1.a = 10;
        e1.b = 10.23;
        e1.c = 'a';
        System.out.println(e1.a);
        System.out.println(e1.b);
        System.out.println(e1.c);

        E121Variables e2 = new E121Variables();
        e2.a = 100;
        e2.b = 100.23;
        e2.c = 's';
        System.out.println(e2.a);
        System.out.println(e2.b);
        System.out.println(e2.c);

    }
}
