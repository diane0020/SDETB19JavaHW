package org.example.e83;

public class E83NestedForLoops {
    public static void main(String[] args) {

        for (int i=1; i<=5; i++){
            for (int j=1; j<=10; j++){
                int result = i * j;

                System.out.print(result + " ");
            }
            System.out.println();
        }


    }

    }

